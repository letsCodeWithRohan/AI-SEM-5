import numpy as np

arr = np.array([1,2,3])

print(arr)  # Output: [1 2 3]

print(np.sin(arr)) # Output : [0.84147098 0.90929743 0.14112001]

print(np.cos(arr)) # Output : [ 0.54030231 -0.41614684 -0.9899925 ]

print(np.tan(arr)) # Output : [ 1.55740772 -2.18503986 -0.14254654]

print(np.radians(30)) # Output : 0.52359878

print(np.degrees(0.52359878)) # Output : 30.00000025
